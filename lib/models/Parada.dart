class Parada {
  final double lat;
  final double long;
  final String rua;
  final String bairro;
  final int numero;
  final String nome;

  const Parada({
    required this.lat,
    required this.long,
    required this.rua,
    required this.bairro,
    required this.numero,
    required this.nome,
  });
}
