class Parada {
  final double lat;
  final double long;
  final String rua;
  final String cidade;
  final int numero;
  final String nome;
  final int codeBus;

  const Parada({
    required this.lat,
    required this.long,
    required this.rua,
    required this.cidade,
    required this.numero,
    required this.nome,
    required this.codeBus,
  });
}
